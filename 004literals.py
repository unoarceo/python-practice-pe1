print()

#hexa
print(0x123)
print()

#float-normal
print(5.0)
print()

#float-exponential
print(3e8)
print()

#string-escape character
print("I love \"Monty Python\"")
print('I\'m Monty Python')
print()

#string-single/double quote
print('I love "Monty Python"')
print("I'm Monty Python")
print()

#boolean
print(True > False)
print(True < False)
print()

