# One argument
print("One argument!")
print()

# Escape character
print("The quick brown fox\nis just very quick.")
print()

# 2 print lines
print("First line.")
print("Second line.")
print()

# Two or more arguments
print("Hello,", "Arjay!")
print()

# Keyword argument = end
print("My name is", end=" ")
print("Arjay.")
print()

# Keyword argument = end = \n
print("My name is", end="\n")
print("Arjay.")
print()

# Keyword argument = end = rand
print("My name is", end="mariah carey , ")
print("Arjay.")
print()

# Keyword argument = sep
print("Hello", "Arjay!","Are","you","a","beginner?",sep="*")
print()

# Keyword argument - MIXED
print("My", "name", "is", sep="_", end="*")
print("Monty", "Python.", sep="*", end="*\n")