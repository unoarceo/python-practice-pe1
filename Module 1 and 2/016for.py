# range() with 1 argument
for i in range(10):
    print("The value of i is currently", i)
print()

# range() with 2 arguments
for i in range(2, 8):
    print("The value of i is currently", i)
print()

# range() with 3 arguments
# the third argument is the increment
for i in range(2, 8, 3):
    print("The value of i is currently", i)
print()